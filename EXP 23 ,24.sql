USE SSE;
CREATE TABLE PATIENT(
PATIENT_ID INT,
PATIENT_NAME VARCHAR(20),
PATIENT_AGE INT,
PATIENT_GENDER VARCHAR(1),
PATIENT_ADDRESS VARCHAR(20),
PATIENT_DISEASE VARCHAR(20),
DOCTOR_ID int
);
ALTER TABLE PATIENT
ADD PRIMARY KEY(PATIENT_ID);

CREATE TABLE DOCTOR(
DOCTOR_ID INT,
NAME VARCHAR(20),
AGE INT,
GENDER VARCHAR(1),
ADDRESS VARCHAR(20)
);
ALTER TABLE DOCTOR
ADD PRIMARY KEY(DOCTOR_ID);

ALTER TABLE PATIENT
ADD FOREIGN KEY(DOCTOR_ID) REFERENCES DOCTOR(DOCTOR_ID);

CREATE TABLE LAB(
LAB_NO VARCHAR(10),
PATIENT_ID VARCHAR(10),
DOCTOR_ID VARCHAR(10),
DATE DATE,
AMOUNT int
);
ALTER TABLE LAB
ADD PRIMARY KEY(LAB_NO);

ALTER TABLE LAB
ADD FOREIGN KEY(PATIENT_ID) REFERENCES PATIENT(PATIENT_ID);

ALTER TABLE LAB
ADD FOREIGN KEY(DOCTOR_ID) REFERENCES DOCTOR(DOCTOR_ID);

