USE SSE;

# 1. Using WHILE : 
DELIMITER $$
CREATE FUNCTION WHILE_DEMONSTRATION(value CHAR(1))
RETURNS CHAR(20)
DETERMINISTIC
BEGIN
	DECLARE str CHAR(100);
    DECLARE i INT;
    SET str = ' ';
    SET i = 1;
    label: 
	WHILE i <= 5 DO
		SET str = CONCAT(str,value);
        SET i = i + 1;
	END WHILE label;
    RETURN str;
END; $$
DELIMITER ;

SELECT WHILE_DEMONSTRATION('A');

# 2. USING REPEAT

DELIMITER $$
CREATE FUNCTION REPEAT_DEMO(INC INT)
RETURNS CHAR(40)
DETERMINISTIC
BEGIN
	DECLARE INCOME INT;
    SET INCOME = 0;
    DECLARE RESULT CHAR(40);
    SET RESULT = 'INCPOME > 4000';
    label1:
	REPEAT
		SET INCOME = INCOME + INC;
        label1;
		UNTIL INCOME > 4000
    END REPEAT;
    RETURN RESULT;
END;
DELIMITER //