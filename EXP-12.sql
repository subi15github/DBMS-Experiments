CREATE FUNCTION CALCINCOME(START_VALUE INT)
RETURNS INT
DETERMINISTIC
BEGIN
   DECLARE INCOME INT;
   SET INCOME=0;
   LABEL1:LOOP
     SET INCOME=INCOME+START_VALUE;
     IF INCOME<4000 THEN
        ITERATE LABEL1;
	 END IF;
     LEAVE LABEL1;
	END LOOP LABEL1;
    RETURN INCOME;
 END;//
