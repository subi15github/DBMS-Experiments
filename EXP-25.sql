CREATE DATABASE COLLEGE;
USE COLLEGE;

CREATE TABLE STUDENT(
	REG_NO INT,
	NAME VARCHAR(15),
    MOBILE_NO INT,
    ADDRESS VARCHAR(50),
    DEGREE VARCHAR(15),
    MODULE VARCHAR(15),
    GRADE CHAR(2),
    PRIMARY KEY(REG_NO)
    );

CREATE TABLE MODULE(
	MODULE_CODE VARCHAR(4),
    MODULE_NAME VARCHAR(15),
    CREDIT INT,
    LEADER VARCHAR(15),
    TEACHING_STAFF VARCHAR(15),
    STAFF_DEPT VARCHAR(15),
    LECTURE_CODE VARCHAR(4),
    PRIMARY KEY(MODULE_CODE)
    
    );
    
CREATE TABLE LECTURE_PLAN(
	PLAN_CODE VARCHAR(4),
	TEACHING_STAFF VARCHAR(15),
    MODULE_NAME VARCHAR(15),
    MODULE_CODE VARCHAR(4),
    CHAPTER_COUNT INT,
    PRIMARY KEY(PLAN_CODE)
    );
    
CREATE TABLE COURSE (
	COURSE_CODE VARCHAR(4),
    COURSE_NAME VARCHAR(15),
    MODULE_CODE VARCHAR(4),
    PRIMARY KEY(COURSE_CODE)
    );
  
CREATE TABLE LECTURER(
	LECTURER_NO VARCHAR(4),
    LECTURER_NAME VARCHAR(15),
    GENDER CHAR(1),
    DOB DATE,
    DOJ DATE,
    MOBILE_NO INT,
    DEPT_NO VARCHAR(4),
    PRIMARY KEY(LECTURER_NO)
    );
    
CREATE TABLE DEPARTMENT(
	DEPT_NO VARCHAR(4),
    DEPT_NAME VARCHAR(15),
    DEPT_HEAD VARCHAR(15),
    PRIMARY KEY(DEPT_NO)
    );
    
CREATE TABLE GRADE(
	MODULE_CODE VARCHAR(4),
    COURSE_CODE VARCHAR(4),
    REG_NO INT,
    GRADE CHAR(2),
    SEM_NO INT
    );
    
CREATE TABLE PREREQUISITE(
	COURSE_NO VARCHAR(4),
    PREREQ_COURSE_NO VARCHAR(4),
    MODULE_NO VARCHAR(4),
    PRIMARY KEY(PREREQ_COURSE_NO)
    );

    
    